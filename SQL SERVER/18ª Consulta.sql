-- Transactions (mais utilizados para DBAs): É uma ação realizada dentro do banco de dados com o uso de auxiliares como BEGIN TRANSACTION, ROLLBACK TRANSICTION e COMMIT
-- COMMIT: Comando TCL para efetivar uma transação no banco (Após o COMMIT ele não tem mais como fazer ROLLBACK)
-- ROLLBACK: Comando TCL para desfazer uma transação no banco
-- BEGIN TRANSECTION: Inicio de uma transaction



